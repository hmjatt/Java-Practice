
public class ArithmaticExpressions {
	public static void main(String[] args) {

		
		int friends = 10;
		
		friends = friends + 1;
		System.out.println(friends);
		
		friends = friends - 1;
		System.out.println(friends);
		
		friends = friends * 2;
		System.out.println(friends);
		
		friends = friends / 5;
		System.out.println(friends);
		
		friends = friends % 3;
		System.out.println(friends);
		
		friends--;
		System.out.println(friends);
		
		friends++;
		System.out.println(friends);

	}
}
