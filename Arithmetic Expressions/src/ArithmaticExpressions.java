
public class ArithmaticExpressions {
	public static void main(String[] args) {
		  
		 // expression =     operands & operators
		 // operands =  values, variables, numbers, quantity
		 // operators = + - * / % 

		
		int friends = 10;
		
		friends = friends + 1;
		System.out.println(friends);
		
		friends = friends - 1;
		System.out.println(friends);
		
		friends = friends * 2;
		System.out.println(friends);
		
		friends = friends / 5;
		System.out.println(friends);
		
		friends = friends % 3;
		System.out.println(friends);

	}
}
